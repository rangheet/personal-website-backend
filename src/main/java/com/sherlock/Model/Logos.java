package com.sherlock.Model;

public class Logos {

    private String Github;
    private String LinkedIn;
    private String Endurance;
    private String Fintech;
    private String ISRO;
    private String Synapse;
    private String AgariaHeetRakshak;

    public Logos(String github, String linkedIn, String endurance, String fintech, String Isro, String synapse, String agariaHeetRakshak) {
        Github = github;
        LinkedIn = linkedIn;
        Endurance = endurance;
        Fintech = fintech;
        ISRO = Isro;
        Synapse = synapse;
        AgariaHeetRakshak = agariaHeetRakshak;
    }

    public String getGithub() {
        return Github;
    }

    public void setGithub(String github) {
        Github = github;
    }

    public String getLinkedIn() {
        return LinkedIn;
    }

    public void setLinkedIn(String linkedIn) {
        LinkedIn = linkedIn;
    }

    public String getEndurance() {
        return Endurance;
    }

    public void setEndurance(String endurance) {
        Endurance = endurance;
    }

    public String getFintech() {
        return Fintech;
    }

    public void setFintech(String fintech) {
        Fintech = fintech;
    }

    public String getISRO() {
        return ISRO;
    }

    public void setISRO(String ISRO) {
        this.ISRO = ISRO;
    }

    public String getSynapse() {
        return Synapse;
    }

    public void setSynapse(String synapse) {
        Synapse = synapse;
    }

    public String getAgariaHeetRakshak() {
        return AgariaHeetRakshak;
    }

    public void setAgariaHeetRakshak(String agariaHeetRakshak) {
        AgariaHeetRakshak = agariaHeetRakshak;
    }




}
